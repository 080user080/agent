def _registry_with_scripted_responses(responses):
    """Реєстр з одним CallableProvider, який повертає відповіді по черзі."""
    responses = list(responses)
    calls = []

    def handler(request: ChatRequest):
        calls.append(request)
        if not responses:
            return "no more scripted responses"
        next_resp = responses.pop(0)
        if isinstance(next_resp, ChatResponse):
            return next_resp
        return next_resp

    provider = CallableProvider(handler=handler)
    reg = ProviderRegistry()
    reg.register(provider)
    return reg, calls


class TestCreateTaskSpec:
    def test_happy_path_single_round(self):
        reg, calls = _registry_with_scripted_responses(
            [_make_llm_json(goal="Django CRUD", domain="code")]
        )
        spec = create_task_spec_from_tz("Напиши CRUD для User", registry=reg)
        assert spec.goal == "Django CRUD"
        assert spec.domain == DOMAIN_CODE
        assert spec.raw_tz == "Напиши CRUD для User"
        assert len(calls) == 1

    def test_empty_tz_raises(self):
        reg, _ = _registry_with_scripted_responses([])
        with pytest.raises(ValueError):
            create_task_spec_from_tz("   ", registry=reg)

    def test_provider_error_raises_intake_error(self):
        def failing_handler(request: ChatRequest):
            return ChatResponse(finish_reason="error", error="boom")

        reg = ProviderRegistry()
        reg.register(CallableProvider(handler=failing_handler))
        with pytest.raises(IntakeError):
            create_task_spec_from_tz("something", registry=reg)

    def test_clarification_round_then_resolved(self):
        q_response = json.dumps(
            {
                "goal": "unclear",
                "domain": "unknown",
                "clarification_questions": [
                    {"question": "Який стек?", "options": ["Django", "Flask"]}
                ],
            }
        )
        final_response = _make_llm_json(goal="Django CRUD", domain="code")
        reg, calls = _registry_with_scripted_responses([q_response, final_response])
        answers = []

        def ask_user(question: str, options):
            answers.append((question, tuple(options)))
            return "Django"

        spec = create_task_spec_from_tz(
            "напиши crud",
            registry=reg,
            ask_user=ask_user,
        )
        assert spec.domain == DOMAIN_CODE
        assert len(calls) == 2
        assert answers == [("Який стек?", ("Django", "Flask"))]
        assert len(spec.clarifications) == 1
        assert spec.clarifications[0].answer == "Django"

    def test_clarification_without_ask_user_breaks_loop(self):
        """Якщо є clarifications але нема ask_user — беремо spec як є."""
        with_q = json.dumps(
            {
                "goal": "tentative",
                "domain": "code",
                "clarification_questions": [{"question": "щось?"}],
            }
        )
        reg, calls = _registry_with_scripted_responses([with_q])
        spec = create_task_spec_from_tz("TZ", registry=reg, ask_user=None)
        assert spec.goal == "tentative"
        assert len(calls) == 1

    def test_max_rounds_exhausted_raises(self):
        """Якщо LLM нескінченно просить уточнення і в кожному раунді spec=None."""
        always_invalid = json.dumps(
            {
                "goal": "",
                "domain": "unknown",
                "clarification_questions": [{"question": "?"}],
            }
        )
        reg, _ = _registry_with_scripted_responses([always_invalid] * 5)

        def ask_user(q, opts):
            return ""

        with pytest.raises(IntakeError):
            create_task_spec_from_tz(
                "TZ",
                registry=reg,
                ask_user=ask_user,
                max_rounds=2,
            )

    def test_invalid_max_rounds(self):
        reg, _ = _registry_with_scripted_responses([])
        with pytest.raises(ValueError):
            create_task_spec_from_tz("TZ", registry=reg, max_rounds=0)

    def test_save_to_writes_json(self, tmp_path: Path):
        reg, _ = _registry_with_scripted_responses(
            [_make_llm_json(goal="g", domain="code")]
        )
        out = tmp_path / "subdir" / "spec.json"
        spec = create_task_spec_from_tz("TZ", registry=reg, save_to=out)
        assert out.exists()
        payload = json.loads(out.read_text(encoding="utf-8"))
        assert payload["goal"] == spec.goal
        assert payload["task_id"] == spec.task_id

    def test_permission_auto_all_detected(self):
        """Якщо LLM повернув auto_all — воно зберігається."""
        reg, _ = _registry_with_scripted_responses(
            [
                json.dumps(
                    {"goal": "g", "domain": "code", "permission_mode": "auto_all"}
                )
            ]
        )
        spec = create_task_spec_from_tz("TZ", registry=reg)
        assert spec.permission_mode == PERMISSION_AUTO_ALL


