def _make_llm_json(**fields):
    default = {"goal": "Do thing", "domain": "code"}
    default.update(fields)
    return json.dumps(default, ensure_ascii=False)


class TestParseIntakeResponse:
    def test_plain_json(self):
        raw = _make_llm_json()
        result = parse_intake_response(raw, raw_tz="original tz")
        assert result.spec is not None
        assert result.spec.goal == "Do thing"
        assert result.spec.domain == DOMAIN_CODE
        assert result.spec.raw_tz == "original tz"
        assert result.pending_questions == []

    def test_json_in_markdown_fence(self):
        raw = "Some preamble\n```json\n" + _make_llm_json() + "\n```\nPostscript"
        result = parse_intake_response(raw, raw_tz="x")
        assert result.spec is not None
        assert result.spec.goal == "Do thing"

    def test_json_with_llm_tokens(self):
        raw = (
            "<|channel|>final<|message|>"
            + _make_llm_json(domain="photo_batch")
            + "<|end|>"
        )
        result = parse_intake_response(raw, raw_tz="x")
        assert result.spec is not None
        assert result.spec.domain == DOMAIN_PHOTO_BATCH

    def test_clarification_questions_extracted(self):
        raw = json.dumps(
            {
                "goal": "unclear",
                "domain": "unknown",
                "clarification_questions": [
                    {"question": "Які файли обробляти?", "options": ["JPG", "PNG"]},
                    {"question": "Куди зберігати?"},
                ],
            },
            ensure_ascii=False,
        )
        result = parse_intake_response(raw, raw_tz="x")
        assert result.spec is not None
        assert len(result.pending_questions) == 2
        assert result.pending_questions[0].question == "Які файли обробляти?"
        assert result.pending_questions[0].options == ["JPG", "PNG"]
        assert result.pending_questions[1].question == "Куди зберігати?"
        assert result.pending_questions[1].options == []

    def test_clarification_as_plain_strings(self):
        raw = json.dumps(
            {
                "goal": "unclear",
                "domain": "unknown",
                "clarification_questions": ["Що саме?", " "],
            }
        )
        result = parse_intake_response(raw, raw_tz="x")
        assert len(result.pending_questions) == 1
        assert result.pending_questions[0].question == "Що саме?"

    def test_no_json_raises(self):
        with pytest.raises(IntakeError):
            parse_intake_response("not json at all", raw_tz="x")

    def test_malformed_json_raises(self):
        with pytest.raises(IntakeError):
            parse_intake_response("{not: valid json}", raw_tz="x")

    def test_invalid_spec_with_clarifications_returns_result(self):
        raw = json.dumps(
            {
                "goal": "",
                "domain": "unknown",
                "clarification_questions": [{"question": "Мета?"}],
            }
        )
        result = parse_intake_response(raw, raw_tz="x")
        assert result.spec is None
        assert len(result.pending_questions) == 1

    def test_invalid_spec_without_clarifications_raises(self):
        raw = json.dumps({"goal": "", "domain": "unknown"})
        with pytest.raises(IntakeError):
            parse_intake_response(raw, raw_tz="x")

    def test_previous_clarifications_recorded_in_spec(self):
        raw = _make_llm_json()
        prev = [Clarification(question="q1", answer="a1")]
        result = parse_intake_response(
            raw, raw_tz="x", previous_clarifications=prev
        )
        assert result.spec is not None
        assert len(result.spec.clarifications) == 1
        assert result.spec.clarifications[0].answer == "a1"

    def test_budget_parsed(self):
        raw = json.dumps(
            {
                "goal": "g",
                "domain": "code",
                "budget": {"max_hours": 2.5, "max_cost_usd": 1.0, "max_ai_calls": 10},
            }
        )
        result = parse_intake_response(raw, raw_tz="x")
        assert result.spec is not None
        assert result.spec.budget.max_hours == 2.5
        assert result.spec.budget.max_cost_usd == 1.0
        assert result.spec.budget.max_ai_calls == 10

    def test_scalar_json_raises(self):
        with pytest.raises(IntakeError):
            parse_intake_response("[1,2,3]", raw_tz="x")


