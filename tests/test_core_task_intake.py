class TestPersistence:
    def test_round_trip(self, tmp_path: Path):
        spec = TaskSpec(
            goal="round trip",
            domain=DOMAIN_MIXED,
            deliverables=["a", "b"],
            budget=BudgetHints(max_hours=1.0),
        )
        path = tmp_path / "spec.json"
        save_task_spec(spec, path)
        loaded = load_task_spec(path)
        assert loaded.goal == spec.goal
        assert loaded.domain == DOMAIN_MIXED
        assert loaded.deliverables == ["a", "b"]
        assert loaded.budget.max_hours == 1.0
        assert loaded.task_id == spec.task_id

    def test_save_creates_parent_dirs(self, tmp_path: Path):
        spec = TaskSpec(goal="g")
        deep = tmp_path / "a" / "b" / "c" / "spec.json"
        save_task_spec(spec, deep)
        assert deep.exists()
