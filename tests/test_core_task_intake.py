class TestTaskSpec:
    def test_empty_goal_raises(self):
        with pytest.raises(ValueError):
            TaskSpec(goal="")
        with pytest.raises(ValueError):
            TaskSpec(goal="   ")

    def test_unknown_domain_normalized(self):
        s = TaskSpec(goal="g", domain="martian")
        assert s.domain == DOMAIN_UNKNOWN

    def test_all_allowed_domains_pass(self):
        for d in ALLOWED_DOMAINS:
            s = TaskSpec(goal="g", domain=d)
            assert s.domain == d

    def test_unknown_permission_normalized(self):
        s = TaskSpec(goal="g", permission_mode="wild")
        assert s.permission_mode == PERMISSION_CONFIRM_EACH

    def test_task_id_autogenerated(self):
        s = TaskSpec(goal="g")
        assert s.task_id.startswith("task_")
        assert s.created_at > 0

    def test_task_id_preserved_when_provided(self):
        s = TaskSpec(goal="g", task_id="task_custom_abc", created_at=1234.0)
        assert s.task_id == "task_custom_abc"
        assert s.created_at == 1234.0

    def test_goal_trimmed(self):
        s = TaskSpec(goal="   hello   ")
        assert s.goal == "hello"

    def test_to_dict_round_trip(self):
        original = TaskSpec(
            goal="Django CRUD",
            domain=DOMAIN_CODE,
            domain_sub="django",
            deliverables=["models.py", "views.py"],
            preferred_ai_order=["codex", "windsurf"],
            permission_mode=PERMISSION_AUTO_ALL,
            budget=BudgetHints(max_hours=3.0, max_cost_usd=1.0),
            input_files=["a.py"],
            output_dir="out/",
            raw_tz="TZ text",
        )
        payload = original.to_dict()
        assert payload["goal"] == "Django CRUD"
        assert payload["budget"]["max_hours"] == 3.0
        restored = TaskSpec.from_dict(payload)
        assert restored.goal == original.goal
        assert restored.domain == original.domain
        assert restored.deliverables == original.deliverables
        assert restored.preferred_ai_order == original.preferred_ai_order
        assert restored.permission_mode == original.permission_mode
        assert restored.budget.max_hours == 3.0
        assert restored.task_id == original.task_id

    def test_from_dict_missing_goal_raises(self):
        with pytest.raises(ValueError):
            TaskSpec.from_dict({"goal": ""})


